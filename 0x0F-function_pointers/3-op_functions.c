/**
 * op_add - function to add two integers
 *
 * @a: first operand
 * @b: second operand
 * Return: sum of operands
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to substract two integers
 *
 * @a: first operand
 * @b: second operand
 * Return: substraction of operands
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function multiplication of two integers
 *
 * @a: first operand
 * @b: second operand
 * Return: mulitplication of operands
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function dividion os two integers
 *
 * @a: first operand
 * @b: second operand
 * Return: division of operands
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Remainder of the division
 *
 * @a: first operand
 * @b: second operand
 * Return: remainder of operands
 */
int op_mod(int a, int b)
{
	return (a % b);
}
